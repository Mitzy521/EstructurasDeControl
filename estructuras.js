console.log ("for:")
// separador del ciclo.
for (let o = 1; o <= 10; o++) { 
    //declaracón de la variable, comparación de la variable con el número limite, la suma de un número al valor inicial.
    console.log ("numero " + o)
    //Impresión de el número.
};


console.log ("While:")
//Separador del ciclo.
let x = 1;
//Declaración de la variable.
while (x <= 10) {
    //Comparación del número inicial y el limite de números.
    console.log("Numero " + x)
    //Impresión de los números.
    x++ ;
    //Indicación del número inicial de uno en uno.
};

